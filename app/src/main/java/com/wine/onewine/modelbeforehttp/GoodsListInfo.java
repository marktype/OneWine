public class GoodsListInfo {
    private String goodsId;
    private String goodsImage;
    private String goodsName;
    private double goodsPrcie;
    private double goodsDiscunt;

    public String getGoodsId() {
        return goodsId;
    }

    public void setGoodsId(String goodsId) {
        this.goodsId = goodsId;
    }

    public String getGoodsImage() {
        return goodsImage;
    }

    public void setGoodsImage(String goodsImage) {
        this.goodsImage = goodsImage;
    }

    public String getGoodsName() {
        return goodsName;
    }

    public void setGoodsName(String goodsName) {
        this.goodsName = goodsName;
    }

    public double getGoodsPrcie() {
        return goodsPrcie;
    }

    public void setGoodsPrcie(double goodsPrcie) {
        this.goodsPrcie = goodsPrcie;
    }

    public double getGoodsDiscunt() {
        return goodsDiscunt;
    }

    public void setGoodsDiscunt(double goodsDiscunt) {
        this.goodsDiscunt = goodsDiscunt;
    }
}
